//Switch case statement 
let cas=3;

switch(cas)//based on case value given in the parenthises the case inside the switch block will be executed
{
    case 1:
        console.log("it is case one");
        break;
    case 2:
        console.log("it is case two");
        break;
    case 3: // this will be executed
        console.log("it is case three");
        break;
    default:
        console.log("not found");
}