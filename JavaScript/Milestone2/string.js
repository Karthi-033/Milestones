//This code has string  functions to do various string operations

let a="karthi";
console.log(typeof a);//this gives the data type of the given value
console.log(a.length);//it return the length of the string 
console.log(a.toUpperCase());//this function convert all the characters into uppercase
console.log(a.toLowerCase());//This convert all the characters into lowercase
console.log(a.slice(0,3));//this gives the part of string based on the given range 
console.log(a.substring(2,3));//this gives the substring based on the range 
console.log(a.split("t"));//based on the given string this function will split the string and return array
console.log(a[0]);//by the index value we can access the string .