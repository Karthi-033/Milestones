let a=10;
console.log(-a);//This will print -10
console.log(a++);//this will print 10 and incremented by 1->11
console.log(a--);//this will print 11 and decremented by 1->10
console.log(++a);//this will print 11
console.log(--a);//this will print 10
console.log(typeof a);//this will return type of a ->number